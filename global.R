# counts number consecutive runs of values above a threshold
count_runs <- function(vec, threshold, min_run) {
  runs <- run <- 0
  for (val in vec) {
    run <- if (val >= threshold) run + 1 else 0
    if (run == min_run) runs <- runs + 1
  }
  runs
}


